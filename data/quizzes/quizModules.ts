/**
 * Centralny rejestr modułów quizowych (ścieżki /quizy/[moduleSlug]/[quizSlug]).
 * Treść pytań nie jest duplikowana — tylko questionIds wskazują na bank w questionBanks.ts.
 */

export type QuizQuestionBankKey =
  | "podstawy"
  | "forex"
  | "cfd"
  | "zaawansowane"
  | "materialy"
  | "regulacje";

export type QuizModuleQuizConfig = {
  slug: string;
  title: string;
  shortDescription: string;
  /** Szacowany czas jednego przejścia quizu (minuty), opcjonalnie pod UI. */
  estimatedMinutes?: number;
  /** Ostatni quiz modułu — podsumowanie przekrojowe (badge na hubie). */
  isModuleSummary?: boolean;
  questionIds: readonly string[];
};

/** 25 kolejnych ID dla quizu podsumowującego (np. base-61 … base-85). */
function summaryQuizQuestionIds(prefix: string, start: number): readonly string[] {
  return Array.from({ length: 25 }, (_, i) => `${prefix}-${start + i}`);
}

export type QuizModuleConfig = {
  slug: string;
  title: string;
  description: string;
  questionBank: QuizQuestionBankKey;
  quizzes: readonly QuizModuleQuizConfig[];
};

