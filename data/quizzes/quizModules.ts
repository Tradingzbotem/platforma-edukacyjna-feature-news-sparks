const MODULE_BY_SLUG: ReadonlyMap<string, QuizModuleConfig> = new Map(
  QUIZ_MODULES.map((m) => [m.slug, m]),
);

/** Szybkie sprawdzenie, czy slug modułu jest częścią rejestru modułowego. */
export function isRegisteredQuizModuleSlug(slug: string): boolean {
  return MODULE_BY_SLUG.has(slug);
}

export function getQuizModuleBySlug(slug: string): QuizModuleConfig | undefined {
  return MODULE_BY_SLUG.get(slug);
}
